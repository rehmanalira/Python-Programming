import re
patt=re.compile(r'\d')
str='''
name =jutt
email= jutta@gmail.com    
email= las@gmail.com
93201

'''
matches=patt.finditer(str)
for match in matches:
    print(match)

lst = re.findall('\S+@\S+', str)
print(lst)

